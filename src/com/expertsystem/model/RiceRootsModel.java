package com.expertsystem.model;

public class RiceRootsModel {

	private String slowGrowth;
	private String yellowLeaves;
	private String leavesDie;
	private String darkRoots;
	private String limpRoots;
	
	public String getSlowGrowth() {
		return slowGrowth;
	}
	public void setSlowGrowth(String slowGrowth) {
		this.slowGrowth = slowGrowth;
	}
	public String getYellowLeaves() {
		return yellowLeaves;
	}
	public void setYellowLeaves(String yellowLeaves) {
		this.yellowLeaves = yellowLeaves;
	}
	public String getLeavesDie() {
		return leavesDie;
	}
	public void setLeavesDie(String leavesDie) {
		this.leavesDie = leavesDie;
	}
	public String getDarkRoots() {
		return darkRoots;
	}
	public void setDarkRoots(String darkRoots) {
		this.darkRoots = darkRoots;
	}
	public String getLimpRoots() {
		return limpRoots;
	}
	public void setLimpRoots(String limpRoots) {
		this.limpRoots = limpRoots;
	}
	
	
}
