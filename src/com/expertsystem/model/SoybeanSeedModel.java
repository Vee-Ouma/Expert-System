package com.expertsystem.model;

public class SoybeanSeedModel {

	private String dampingOff;
	private String bruisedStems;
	private String rottingTissue;
	private String sunkenLesion;
	public String getDampingOff() {
		return dampingOff;
	}
	public void setDampingOff(String dampingOff) {
		this.dampingOff = dampingOff;
	}
	public String getBruisedStems() {
		return bruisedStems;
	}
	public void setBruisedStems(String bruisedStems) {
		this.bruisedStems = bruisedStems;
	}
	public String getRottingTissue() {
		return rottingTissue;
	}
	public void setRottingTissue(String rottingTissue) {
		this.rottingTissue = rottingTissue;
	}
	public String getSunkenLesion() {
		return sunkenLesion;
	}
	public void setSunkenLesion(String sunkenLesion) {
		this.sunkenLesion = sunkenLesion;
	}
	
	
}
