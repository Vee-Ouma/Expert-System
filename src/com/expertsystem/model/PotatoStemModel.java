package com.expertsystem.model;

public class PotatoStemModel {

	private String stuntedStem;
	private String smallLeaves;
	private String blackStems;
	private String vineWilting;
	private String brittleStems;
	private String brownLessions;
	public String getStuntedStem() {
		return stuntedStem;
	}
	public void setStuntedStem(String stuntedStem) {
		this.stuntedStem = stuntedStem;
	}
	public String getSmallLeaves() {
		return smallLeaves;
	}
	public void setSmallLeaves(String smallLeaves) {
		this.smallLeaves = smallLeaves;
	}
	public String getBlackStems() {
		return blackStems;
	}
	public void setBlackStems(String blackStems) {
		this.blackStems = blackStems;
	}
	public String getVineWilting() {
		return vineWilting;
	}
	public void setVineWilting(String vineWilting) {
		this.vineWilting = vineWilting;
	}
	public String getBrittleStems() {
		return brittleStems;
	}
	public void setBrittleStems(String brittleStems) {
		this.brittleStems = brittleStems;
	}
	public String getBrownLessions() {
		return brownLessions;
	}
	public void setBrownLessions(String brownLessions) {
		this.brownLessions = brownLessions;
	}
	
	
}
