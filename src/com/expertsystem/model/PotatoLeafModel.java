package com.expertsystem.model;

public class PotatoLeafModel {

	private String rollingLeaves;
	private String leatheryLeaves;
	private String deadSpots;
	private String stuntingPlant;
	private String yellowVines;
	private String leafYellowing;
	private String deadPlants;
	
	public String getRollingLeaves() {
		return rollingLeaves;
	}
	public void setRollingLeaves(String rollingLeaves) {
		this.rollingLeaves = rollingLeaves;
	}
	public String getLeatheryLeaves() {
		return leatheryLeaves;
	}
	public void setLeatheryLeaves(String leatheryLeaves) {
		this.leatheryLeaves = leatheryLeaves;
	}
	public String getDeadSpots() {
		return deadSpots;
	}
	public void setDeadSpots(String deadSpots) {
		this.deadSpots = deadSpots;
	}
	public String getStuntingPlant() {
		return stuntingPlant;
	}
	public void setStuntingPlant(String stuntingPlant) {
		this.stuntingPlant = stuntingPlant;
	}
	public String getYellowVines() {
		return yellowVines;
	}
	public void setYellowVines(String yellowVines) {
		this.yellowVines = yellowVines;
	}
	public String getLeafYellowing() {
		return leafYellowing;
	}
	public void setLeafYellowing(String leafYellowing) {
		this.leafYellowing = leafYellowing;
	}
	public String getDeadPlants() {
		return deadPlants;
	}
	public void setDeadPlants(String deadPlants) {
		this.deadPlants = deadPlants;
	}
	
}
